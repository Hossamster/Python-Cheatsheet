#Loops 
# for loop and the while loop 

for n in range(1,8):
    print(n) #print from 1 to 7

for x in "coffee":
    print(x) #print c o f f e e 

for k in range(7):
    print(k) #from 0 to 6

for m in range(1,10,2):
    print(m) #from 1 to 9 and step 2 positions

for j in range(7,0,-1):
    print(j) #go down from 7 to 1 with step 1 position

r = range(1,10)
print(list(r)) 